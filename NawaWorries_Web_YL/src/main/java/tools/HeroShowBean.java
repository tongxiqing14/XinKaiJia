public class HeroShowBean {

    private int fighter_id;
    private boolean got_flag;
    private int upgrade_level;

    public HeroShowBean() {
    }

    public HeroShowBean(int fighter_id, boolean got_flag, int upgrade_level) {
        this.fighter_id = fighter_id;
        this.got_flag = got_flag;
        this.upgrade_level = upgrade_level;
    }

    public int getFighter_id() {
        return fighter_id;
    }

    public void setFighter_id(int fighter_id) {
        this.fighter_id = fighter_id;
    }

    public boolean isGot_flag() {
        return got_flag;
    }

    public void setGot_flag(boolean got_flag) {
        this.got_flag = got_flag;
    }

    public int getUpgrade_level() {
        return upgrade_level;
    }

    public void setUpgrade_level(int upgrade_level) {
        this.upgrade_level = upgrade_level;
    }

    @Override
    public String toString() {
        return "HeroShowBean{" +
                "fighter_id=" + fighter_id +
                ", got_flag=" + got_flag +
                ", upgrade_level=" + upgrade_level +
                '}';
    }
}
