public class FighterBean {

    private int fighter_id = 0;
    private int type = 0;
    private int got_flag = 0;  /**是否购买*/
    private boolean select_flag = false;     /**是否选中*/

    public FighterBean() {
    }

    public FighterBean(int fighter_id, int type, int got_flag, boolean select_flag) {
        this.fighter_id = fighter_id;
        this.type = type;
        this.got_flag = got_flag;
        this.select_flag = select_flag;
    }

    public int getFighter_id() {
        return fighter_id;
    }

    public void setFighter_id(int fighter_id) {
        this.fighter_id = fighter_id;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int isGot_flag() {
        return got_flag;
    }

    public void setGot_flag(int got_flag) {
        this.got_flag = got_flag;
    }

    public boolean isSelect_flag() {
        return select_flag;
    }

    public void setSelect_flag(boolean select_flag) {
        this.select_flag = select_flag;
    }

    @Override
    public String toString() {
        return "FighterBean{" +
                ", fighter_id=" + fighter_id +
                ", type=" + type +
                ", got_flag=" + got_flag +
                ", select_flag=" + select_flag +
                '}';
    }
}
